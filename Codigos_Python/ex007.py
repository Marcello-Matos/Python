#Desenvolva um programa que leia as duas notas de um aluno. Calcule a sua média.
n = float(input('Digite a primeira nota do aluno:'))
n1 = int(input('Digite a segunda nota do aluno:'))
n2 = (n + n1) / 2
print('A primeira nota do aluno foi:{} \nA segunda nota foi:{} \nE a sua média é:{}'.format(n, n1, n2))
